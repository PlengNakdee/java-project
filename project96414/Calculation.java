public class Calculation {

    public int add(int a, int b) {
        int result;
        result = a + b;
        return result;
    }

    public int subtract(int a, int b) {
        int result;
        result = a - b;
        return result;
    }

        public int multiply(int a, int b) {
        int result;
        result = a * b;
        return result;
    }

        public double divide(int a, int b) {
        double result;
        result = (double)a / (double)b;
        return result;
    }
}
